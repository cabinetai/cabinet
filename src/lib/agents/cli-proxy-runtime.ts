export function resolveCLIProxyAsset(
  platform: NodeJS.Platform = process.platform,
  arch: string = process.arch
): ReleaseAsset {
  if (!(["darwin", "linux", "win32"] as string[]).includes(platform)) {
    throw new Error(`CLIProxyAPI is not supported on ${platform}.`);
  }
  if (arch !== "x64" && arch !== "arm64") {
    throw new Error(`CLIProxyAPI is not supported on ${platform}/${arch}.`);
  }
  return RELEASE_ASSETS[`${platform as SupportedPlatform}-${arch}`];
}

export function getCLIProxyPaths(root = path.join(CABINET_INTERNAL_DIR, "cli-proxy")): CLIProxyPaths {
  const binaryDir = path.join(root, "bin", CLI_PROXY_VERSION);
  return {
    root,
    binaryDir,
    binaryPath: path.join(binaryDir, process.platform === "win32" ? "cli-proxy-api.exe" : "cli-proxy-api"),
    authDir: path.join(root, "auth"),
    configPath: path.join(root, "config.yaml"),
    runtimePath: path.join(root, "runtime.json"),
    installManifestPath: path.join(binaryDir, "install.json"),
    licensePath: path.join(binaryDir, "LICENSE"),
  };
}

function yamlString(value: string): string {
  return JSON.stringify(value);
}

